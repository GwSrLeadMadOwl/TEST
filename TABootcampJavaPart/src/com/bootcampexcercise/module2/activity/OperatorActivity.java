package com.bootcampexcercise.module2.activity;

public class OperatorActivity {
    public static void main(String args[]){
        int a=10;
        int b=15;

        sum(a,b);
    }

    public static void sum(int a, int b){
        System.out.println(a + " + " + b);
        System.out.println(a+b);
    }
}
